package day1;

public class BooleanVariables{
    public static void main(String[] args) {
        boolean isLocked;
        boolean isOdd;
        boolean isRaining;
        boolean hasShield;
        isLocked = false;
        isOdd = true;
        isRaining = false;
        hasShield = true;
        System.out.println("isLocked: " + isLocked);
        System.out.println("isOdd: " + isOdd);
        System.out.println("isRaining: " + isRaining);
        System.out.println("hasShield: " + hasShield);


    }
}